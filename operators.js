// 1. arithmetic

let x = 10;
let y = 3;

console.log(x + y);
console.log(x - y);
console.log(x / y);
console.log(x * y);
console.log(x % y);
console.log(x ** y); //x^y

// increment (++)
console.log(x);
console.log(++x); //displays 11 
console.log(x++); //display 10 then adds 1 to x

console.log(x--); // displays value of x
console.log(--x); // subtracts 1 from x and then displays value of x


